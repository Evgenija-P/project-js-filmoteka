// Close Film Modal

const { modalWindow, buttonModalClose, buttonModalOpen } = {
  modalWindow: document.querySelector('[data-modal]'),
  // buttonModalOpen: document.querySelector('[button-modal-open]'),
  buttonModalClose: document.querySelector('[button-modal-close]'),
};

// buttonModalOpen.addEventListener('click', openFilmModal);
buttonModalClose.addEventListener('click', closeFilmModal);
modalWindow.addEventListener('click', onBackdropModalClick);

function openFilmModal() {
  window.addEventListener('keydown', onEscKeyPress);
  modalWindow.classList.remove('is-hidden');
}

function closeFilmModal() {
  window.removeEventListener('keydown', onEscKeyPress);
  modalWindow.classList.add('is-hidden');
}

function onBackdropModalClick(e) {
  if (e.currentTarget === e.target) {
    closeFilmModal();
  }
}

function onEscKeyPress(e) {
  if (e.code === 'Escape') {
    closeFilmModal();
  }
}
